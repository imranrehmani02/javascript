// Logical Operators:- Logical operators are used to determine the logic between variable and values.



/*
    OR ( || )   either one true 
    AND ( && )  all condition true
    NOT ( ! )   not
 */


let x = 25;

console.log("OR:-  ( || )")
if (x > 20 || x < 15) {
    console.log("Condition true")
}
else {
    console.log("Condition fail")
}



console.log("AND:-  ( && )")
if (x > 20 && x < 15) {
    console.log("Condition true")
} else {
    console.log("Condition fail")
}



console.log("NOT:-  ( ! )")
if (x != 30) {
    console.log("Condition true")
}
else {
    console.log("Condition fail")
}
